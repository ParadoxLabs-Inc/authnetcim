namespace ParadoxLabs\Authnetcim\Observer;

/**
 * CheckoutSuccessClearCcCidObserver Class
 */
class CheckoutSuccessClearCcCidObserver implements \Magento\Framework\Event\ObserverInterface
{
    /**
     * @var \Magento\Checkout\Model\Session
     */
    protected $checkoutSession;

    /**
     * @param \Magento\Checkout\Model\Session $checkoutSession
     */
    public function __construct(
        \Magento\Checkout\Model\Session $checkoutSession
    ) {
        $this->checkoutSession = $checkoutSession;
    }

    /**
     * Remove any cc_cid value from checkout session after order success.
     *
     * This applies only to the Hyva Checkout/Magewire checkout implementation (as of 2023-11-30).
     *
     * @param \Magento\Framework\Event\Observer $observer
     * @return void
     */
    public function execute(\Magento\Framework\Event\Observer $observer)
    {
        $this->checkoutSession->setStepData('payment', 'cc_cid', false);
    }
}
